CREATE TABLE TIPO_TRANSACCION (
    ID_TIT          NUMBER(10)   NOT NULL,
    NOMBRE_TIT      VARCHAR2(20) NOT NULL,
    DESCRIPCION_TIT VARCHAR2(50) NOT NULL,
