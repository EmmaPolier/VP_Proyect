CREATE TABLE ESTADO_CUR (
    ID_CUR          NUMBER(10)    NOT NULL,
    NOMBRE_CUR      VARCHAR2(30)  NOT NULL,
    DESCRIPCION_CUR VARCHAR2(150) NOT NULL,
    CONSTRAINT ESTADO_CUR_PK PRIMARY KEY (ID_CUR)
