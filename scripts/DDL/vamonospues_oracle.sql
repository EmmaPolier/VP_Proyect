    CONSTRAINT PK_METODO_PAGO PRIMARY KEY (ID_MPA)
);

CREATE TABLE MARCA_VEHICULO (
    ID_MAR     NUMBER(10)   NOT NULL,
    NOMBRE_MAR VARCHAR2(25) NOT NULL,
    CONSTRAINT PK_MARCA_VEHICULO PRIMARY KEY (ID_MAR)
);

CREATE TABLE MODELO_VEHICULO (
    ID_MOD     NUMBER(10)   NOT NULL,
    NOMBRE_MOD VARCHAR2(50) NOT NULL,
